public class FormatandoComPrintf {
  public static void main(String[] args) {
    String nome = "Eduardo";
    System.out.printf("Seu nome e: %s%n", nome);

    int idade = 19;
    System.out.printf("Sua idade: %d%n", idade);

    float peso = 64.55f;
    System.out.printf("Seu peso: %8.2f%n", peso);
 }
}